    public void loginMethod(){
        // TODO add your handling code here:
        usernameLogin = txtUsernameLogin.getText();
        passwordLogin = txtPasswordLogin.getText();
        
        try{
            
            if(usernameLogin.equals("") || passwordLogin.equals("")){
                try{
                   JOptionPane.showMessageDialog(rootPane, "Sorry You need to Fillin All fields on the Form"); 
                }catch(HeadlessException ex){
                    JOptionPane.showMessageDialog(null,ex);
                }
            }else{
                try{
                    //getConnection(); //same class
                    conn = DBConnection_Pharmacy_POS.getConnection(); //different class
                    //conn = DBConnection.getConnection(); //different class
                    st = conn.createStatement();
                    rs = st.executeQuery("SELECT UserRole, Username, UserPassword FROM USERS");
                    while(rs.next()){
                        
                        this.usernameLoginDB = rs.getString("Username");
                        this.passwordLoginDB = rs.getString("UserPassword");
                        this.userRoleDB = rs.getString("UserRole");
                        
                        if(usernameLogin.equals(usernameLoginDB) && passwordLogin.equals(passwordLoginDB) && userRoleDB.equals("Employee")){
                            //RITER TO A FILE
                            //RecordLogg();
                            JOptionPane.showMessageDialog(null, "Hello "+usernameLogin+" you have successfully logged in as "+userRoleDB);
                            recordLoggs();
                                    
                            WelcomeTeller tellerWelcome = new WelcomeTeller();
                            tellerWelcome.setVisible(true);
                            tellerWelcome.loggedinUser(usernameLogin);
                            dispose();
                            break;
                            
                        }if(usernameLogin.equals(usernameLoginDB) && passwordLogin.equals(passwordLoginDB) && userRoleDB.equals("Admin")){
                            //RITER TO A FILE
                            //RecordLogg();
                            JOptionPane.showMessageDialog(null, "Hello "+usernameLogin+" you have successfully logged in as "+userRoleDB);
                            recordLoggs();
                            
                            ManageStock products = new ManageStock();
                            products.setVisible(true);
                            products.loggedinUser(usernameLogin);
                            dispose();
                            break;
                            
                        }else if(!usernameLogin.equals(usernameLoginDB) || !passwordLogin.equals(passwordLoginDB)){
                            JOptionPane.showMessageDialog(null, "Sorry! Either username or password is incorrect = 1");
                            //break;
                        }
                    }//end of whileLoop
                    
                }catch(HeadlessException | SQLException ex){
                    JOptionPane.showMessageDialog(null, "Sorry! Either username or password is incorrect 2!\n"+ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(HomePage.class.getName()).log(Level.SEVERE, null, "Sorry! connection failed please ensure that your database is open."+ ex);
                }
            }//end of else
        }catch(HeadlessException error){
            //JOptionPane.showMessageDialog(null, "Sorry failed to login. \n Either username or password is incorrect." +error);
            JOptionPane.showMessageDialog(null, "Sorry! Either username or password is incorrect = 3!\n"+error); 
        }
    }//end of Login
    
    
